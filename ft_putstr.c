#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;
	int	aux;

	aux = 0;
	i = 0;
	if (!s)
		s = "(null)";
	while (s[i] != '\0')
	{
		aux += write (1, s + i, 1);
		i++;
	}
	return (aux);
}
